buzzer = PWM(Pin(20))   # Buzzer'imizi GP20'ye PWM kullanmak uzere bagliyoruz
buzzer.duty_u16(0)

def playtone(frequency):    # bu fonksiyon ilgili notanin frekansini buzzerin bagli oldugu PWM cikisina iletiyor
    buzzer.duty_u16(2000)   # Aktif buzzer icin ayarlanmistir. Pasif buzzer icin Duty'i artirabilirsiniz (32500 civari yaklasik %50'ye tekabul eder)
    buzzer.freq(frequency)
    
def bequiet():              # bu fonksiyon PWM cikisini kapatarak buzzer'i susturuyor
    buzzer.duty_u16(0)
    
def playsong():             # bu fonksiyon sarki dongumuzu calistiriyor
    for note in song:
        if note[0] == "-":      # Nota yerine cizgi yer aliyorsa S, belirtilen olcu kadar S verilir.
            bequiet()
        elif note[0] == "BBB":  # Bayrak imajinin gosterilecegi anda NOTA harici bir belirtec konulmustur
            oled.fill(0)        # ekranin icerigini temizliyoruz
            oled.blit(fb, 0, 0) # daha onceden hazirda tuttugumuz framebuffer'i ekrana yansitiyoruz
            oled.show()
        else:
            playtone(tones[note[0]])    # ilgili notanin frekansini "dictionary"den cekip fonksiyona iletiyoruz
        sleep(ritm*note[1])             # ilgili notanin olcusu kadar bekleme suresi ekliyoruz.
        

    bequiet()   # dongu tamamlandiktan sonra buzzer'i susturuyoruz
    
playsong()

